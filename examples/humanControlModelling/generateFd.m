function fd = generateFd(model,dist)
    
    nd = [  3  
            4  
            11 
            12 
            23 
            24 
            37 
            38 
            51 
            52 
            71 
            72 
            101
            102
            137
            138
            171
            172
            225
            226];
          
        Ad = [1.6733443186304570e-002
                1.6324210809983902e-002
                1.1964041839227531e-002
                1.1307415760209856e-002
                5.9596967412649691e-003
                5.6419625443116543e-003
                3.0359384041892699e-003
                2.9146051996769605e-003
                1.8472794014596239e-003
                1.7925780287128296e-003
                1.1221209622558972e-003
                1.0997655790593570e-003
                7.0211537798450124e-004
                6.9389730639733335e-004
                5.0768780465024318e-004
                5.0430525965676205e-004
                4.2339418805392991e-004
                4.2163357274295976e-004
                3.5901280692483644e-004
                3.5823244206980394e-004];

    
    wm = (2*pi) / 81.92;
    
    s = tf('s');

    TA1 = 0.1;
    TA2 = 0.8;
    H_Anew  = (1 + TA1 * s)^2 / (1 + TA2 * s)^2;

    Adnew = abs(squeeze(freqresp(H_Anew,nd*wm)));
    Adnewsc = (Ad(1) / Adnew(1)) * Adnew;
    
    Ad = Adnewsc;

        phase =     [1.2690046760626796e+000
                    2.6765830315551451e+000
                    4.5224724484314889e+000
                    1.1221526260572579e+000
                    4.1589549205785321e+000
                    1.6998381053567606e+000
                    1.4078387443648972e+000
                    6.2706350697457980e+000
                    5.9927669712281064e+000
                    6.0569078477642235e-001
                    3.6556175005475383e-001
                    4.8492630088910644e+000
                    9.0557904670738076e-001
                    4.4812241586823349e+000
                    1.4742615967251671e+000
                    5.8832713561564338e+000
                    3.8821594775326815e+000
                    5.1612153013936402e+000
                    8.1263526632283634e-001
                    4.3437431589808417e+000];
                
    fd  = zeros(size(model.measurementTime));
             
    for oo=1:length(phase)
        fd  = fd + Ad(oo) * sin(nd(oo) * wm * (model.measurementTime + 8.09) + phase(oo));
    end
    
    fd = dist * fd;
    
end